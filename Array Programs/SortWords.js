// JavaScript Program to Sort Words in Alphabetical Order

// Using the Array sort() method :
// The sort() method sorts all the elements of the array and returns the sorted array.
// By default, the sort() method sorts the elements in alphabetical order.

const words = [
  'JavaScript',
  'Program',
  'to',
  'Sort',
  'Words',
  'in',
  'Alphabetical',
  'Order'
]

// Sorting the input array using array.sort 
const sortedWords = words.sort()

// Getting the sorter array output 
console.log(sortedWords)
